import NavBar from "@/components/NavBar.vue";
import { getAuth, signOut, onAuthStateChanged } from "firebase/auth";
let auth;
export default {
  data() {
    return {
      isLoggedIn: false,
    };
  },
  components: {
    NavBar,
  },
  inject: ["emitter"],
  methods: {
    signOutTrigger() {
      signOut(auth).then(() => {
        this.$router.push("/");
      });
    },
  },
  mounted() {
    auth = getAuth();
    onAuthStateChanged(auth, (user) => {
      if (user) {
        this.isLoggedIn = true;
      } else {
        this.isLoggedIn = false;
      }
    });
    this.emitter.on("SignOutTrig", () => {
      this.signOutTrigger();
    });
